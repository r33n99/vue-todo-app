<template>
    <form @submit.prevent="onSubmit">
        <input type="text" v-model="title" >
        <button type="submit">Create</button>
    </form>
</template>

<script>
export default {
    data() {
        return {
            title: ''
        }
    },
    methods: {
        onSubmit() {
            console.log('Submit', this.title)
            if(this.title.trim()) {
                const newToDo = {
                    id: Date.now(),
                    title:this.title,
                    completed: false
                }
            this.$emit('add-todo', newToDo)

            }
        }
    }
}
</script>

<style scoped>
form {
    display: flex;

}

input {
    width: 500px;
}
</style>